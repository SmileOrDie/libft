#include "includes/libft.h"

char		*ft_strchr(char const *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != c && s[i] != '\0')
		i++;
	if (s[i] == c)
		return (((char *)s + i));
	return (NULL);
}
